import math 

# Square root function
def square_root(x):
    return math.sqrt(x)

# Power function
def power(base, exponent):
    return math.pow(base, exponent)

# Sin function
def sin(angle):
    return math.sin(math.radians(angle))

# Cos function
def cos(angle):
    return math.cos(math.radians(angle))



