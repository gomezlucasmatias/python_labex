# Function for import  

# To sum two terms 
def add(a, b):
    return a + b

# To subtract between two terms 
def subtract(a, b):
    return a - b 

# To multiply two terms 
def multiply(a, b):
    return a * b 

# To divide two terms 
def divide(a, b):
    if b != 0:
        return a / b 
    else:
        return 'Error: Division by zero'

