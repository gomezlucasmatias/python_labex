# To sum two terms 
def add(a, b):
    return a + b

# To rest two terms 
def substract(a, b):
    return a - b 

# To multiply two terms 
def multiply(a, b):
    return a * b 

# To divide two terms 
def divide(a, b):
    if b != 0:
        return a / substract
    else:
        return 'Error: Division by zero' 

